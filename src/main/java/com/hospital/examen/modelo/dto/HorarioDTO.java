package com.hospital.examen.modelo.dto;

import java.time.LocalTime;

public class HorarioDTO {

    private Long id;
    private String diaSemana;    // Día de la semana
    private LocalTime horaInicio; // Hora de inicio
    private LocalTime horaFin;    // Hora de fin
    private Long medicoId;       // ID del médico asociado (si es necesario)

    // Getters y setters

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getDiaSemana() {
        return diaSemana;
    }

    public void setDiaSemana(String diaSemana) {
        this.diaSemana = diaSemana;
    }

    public LocalTime getHoraInicio() {
        return horaInicio;
    }

    public void setHoraInicio(LocalTime horaInicio) {
        this.horaInicio = horaInicio;
    }

    public LocalTime getHoraFin() {
        return horaFin;
    }

    public void setHoraFin(LocalTime horaFin) {
        this.horaFin = horaFin;
    }

    public Long getMedicoId() {
        return medicoId;
    }

    public void setMedicoId(Long medicoId) {
        this.medicoId = medicoId;
    }
}
